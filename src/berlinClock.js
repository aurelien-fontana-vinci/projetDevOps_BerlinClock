export class Main{

    berlinClock(date){
        var l1 = "0000";

        if(date === 1){
            l1 = "1000";
            return l1;
        } 

        if(date === 2){
            l1 = "1100";
            return l1;
        } 

        if(date === 3){
            l1 = "1110";
            return l1;
        } 

        if(date === 4){
            l1 = "1111";
            return l1;
        } 


    }
}